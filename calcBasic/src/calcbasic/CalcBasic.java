public class CalcBasic {
    float n1, n2, total;
public CalcBasic(){
    n1 = 0;
    n2 = 0;
    total =0;
    }
public void Somar(){
total=n1+n2;
}    
public void Subtrair(){
total=n1-n2;
}  
public void Dividir(){
total=n1/n2;
}  
public void Multiplicar(){
total=n1*n2;
}  
}
 
